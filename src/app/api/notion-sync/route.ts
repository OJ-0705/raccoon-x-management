export async function POST(req: NextRequest) {
  // Verify cron secret if set
  const cronSecret = process.env.CRON_SECRET
  if (cronSecret) {
    const auth = req.headers.get('authorization')
    if (auth !== `Bearer ${cronSecret}`) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }
  }

  const apiKey = (process.env.NOTION_API_KEY || '').trim()

  let samplesMap = new Map<number, string>()
  let source = 'hardcoded'

  if (apiKey) {
    try {
      console.log('[notion-sync] Fetching samples from Notion...')
      const allBlocks: NotionBlock[] = []

      for (const pageId of NOTION_PAGE_IDS) {
        const blocks = await fetchPageBlocks(pageId, apiKey)
        allBlocks.push(...blocks)
      }

      const parsed = parseBlocksToSamples(allBlocks)
      if (parsed.size >= 20) {
        samplesMap = parsed
        source = 'notion'
        console.log(`[notion-sync] Parsed ${parsed.size} samples from Notion`)
      } else {
        console.warn(`[notion-sync] Only ${parsed.size} samples parsed from Notion, falling back to hardcoded`)
        samplesMap = new Map(Object.entries(HARDCODED_SAMPLES).map(([k, v]) => [parseInt(k), v]))
      }
    } catch (err) {
      console.error('[notion-sync] Notion fetch failed, falling back to hardcoded:', err)
      samplesMap = new Map(Object.entries(HARDCODED_SAMPLES).map(([k, v]) => [parseInt(k), v]))
    }
  } else {
    console.log('[notion-sync] NOTION_API_KEY not set, using hardcoded samples')
    samplesMap = new Map(Object.entries(HARDCODED_SAMPLES).map(([k, v]) => [parseInt(k), v]))
  }

  // Upsert all samples to DB
  let saved = 0
  const errors: string[] = []

  for (const [number, text] of samplesMap.entries()) {
    try {
      await prisma.samplePost.upsert({
        where: { number },
        create: { number, text },
        update: { text },
      })
      saved++
    } catch (err) {
      errors.push(`サンプル${number}: ${String(err)}`)
    }
  }

  console.log(`[notion-sync] Saved ${saved} samples (source: ${source})`)

  return NextResponse.json({
    success: true,
    saved,
    source,
    errors: errors.length > 0 ? errors : undefined,
    time: new Date().toISOString(),
  })
}
