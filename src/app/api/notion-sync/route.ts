interface NotionBlock {
  type: string
  [key: string]: unknown
}

function extractPlainText(block: NotionBlock): string {
  const richTextTypes = [
    'paragraph', 'heading_1', 'heading_2', 'heading_3',
    'bulleted_list_item', 'numbered_list_item', 'quote', 'callout',
  ]
  for (const type of richTextTypes) {
    if (block.type === type) {
      const blockData = block[type] as { rich_text?: Array<{ plain_text?: string }> }
      if (blockData?.rich_text) {
        return blockData.rich_text.map(t => t.plain_text || '').join('')
      }
    }
  }
  return ''
}

async function fetchPageBlocks(pageId: string, apiKey: string): Promise<NotionBlock[]> {
  const blocks: NotionBlock[] = []
  let cursor: string | undefined

  do {
    const url = new URL(`${NOTION_API_BASE}/blocks/${pageId}/children`)
    url.searchParams.set('page_size', '100')
    if (cursor) url.searchParams.set('start_cursor', cursor)

    const res = await fetch(url.toString(), {
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Notion-Version': NOTION_VERSION,
      },
    })

    if (!res.ok) {
      console.error(`[notion-sync] Failed to fetch blocks for ${pageId}: ${res.status}`)
      break
    }

    const data = await res.json() as {
      results?: NotionBlock[]
      has_more?: boolean
      next_cursor?: string
    }

    if (data.results) blocks.push(...data.results)
    cursor = data.has_more ? data.next_cursor : undefined
  } while (cursor)

  return blocks
}

/**
 * Parse blocks into sample posts.
 * Splits on heading_2 blocks matching "サンプルN".
 */
function parseBlocksToSamples(blocks: NotionBlock[]): Map<number, string> {
  const samples = new Map<number, string>()
  let currentNumber: number | null = null
  const currentLines: string[] = []

  function flush() {
    if (currentNumber !== null && currentLines.length > 0) {
      const text = currentLines.join('\n').trim()
      if (text) samples.set(currentNumber, text)
    }
  }

  for (const block of blocks) {
    if (block.type === 'heading_2') {
      const text = extractPlainText(block)
      const match = text.match(/^サンプル(\d+)/)
      if (match) {
        flush()
        currentNumber = parseInt(match[1])
        currentLines.length = 0
        continue
      }
      // Heading that is NOT a sample marker → flush and stop (e.g. "サンプル23〜50")
      if (text.includes('サンプル')) {
        flush()
        currentNumber = null
        currentLines.length = 0
      }
      continue
    }

    if (block.type === 'divider') {
      // Divider ends current sample
      flush()
      currentNumber = null
      currentLines.length = 0
      continue
    }

    if (block.type === 'child_page') {
      // Skip child page links
      continue
    }

    if (currentNumber !== null) {
      const text = extractPlainText(block)
      if (text) currentLines.push(text)
    }
  }

  flush()
  return samples
}

